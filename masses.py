# kg to tonnes
def kg_to_t(kg:float):
    t = kg / 1000
    return t

# tonnes to kg
def t_to_kg(t:float):
    kg = t * 1000
    return kg

# kg to pounds
def kg_to_lb(kg:float):
    lb = kg * 2.20462
    return lb

# pounds to kg
def lb_to_kg(lb:float):
    kg = lb / 2.20462
    return kg

# kg to stones
def kg_to_st(kg:float):
    st = kg /6.35029
    return st

# stones to kg
def st_to_kg(st:float):
    kg = st * 6.35029
    return kg

# pound to stones
def lb_to_st(lb:float):
    st = lb / 14
    return st

# stones to pound
def st_to_lb(st:float):
    lb = st * 14
    return lb

# tonnes to stones
def t_to_st(t:float):
    st = t * 157.473
    return st

# stones to tonnes
def st_to_t(st:float):
    t = st / 157.473
    return t

# tonnes to imperial
def t_to_it(t:float):
    it = t / 1.01605
    return it

# imperial to tonnes
def it_to_t(it:float):
    t = it * 1.01605
    return t